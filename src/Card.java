public class Card {

        //------DEFINATIONS---------//
    private char num;
    private char type;

        //----------CONSTRUCTORS----------//

    //That constructors sets null card
    Card(){
        num = 'x';
        type = 'x';
    }

        //------GETTERS---------//
    public char getNum() {return num;}
    public char getType() {return type;}

        //------SETTERS---------//
    public void setNum(char num) {this.num = num;}
    public void setType(char type) {this.type = type;}
}












